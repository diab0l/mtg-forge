package forge.card.cardfactory;


import forge.Card;
import forge.card.spellability.SpellAbility;
import forge.game.player.Player;
import forge.item.CardPrinted;

/**
 * The Interface CardFactoryInterface.
 */
public interface CardFactoryInterface {


    /**
     * <p>
     * copyCard.
     * </p>
     * 
     * @param in
     *            a {@link forge.Card} object.
     * @return a {@link forge.Card} object.
     */
    Card copyCard(Card in);

    /**
     * <p>
     * copySpellontoStack.
     * </p>
     * 
     * @param source
     *            a {@link forge.Card} object.
     * @param original
     *            a {@link forge.Card} object.
     * @param sa
     *            a {@link forge.card.spellability.SpellAbility} object.
     * @param bCopyDetails
     *            a boolean.
     */
    void copySpellontoStack(Card source, Card original, SpellAbility sa, boolean bCopyDetails);

    /**
     * <p>getCard.</p>
     * 
     * @param cardName
     *            a {@link java.lang.String} object.
     * @param owner
     *            a {@link forge.game.player.Player} object.
     * @return a {@link forge.Card} instance, owned by owner; or the special
     *         blankCard
     */
    Card getCard(CardPrinted card, Player owner);
}
